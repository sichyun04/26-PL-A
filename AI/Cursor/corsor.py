def merge_sort(arr):
    def _merge_sort(arr):
        if len(arr) > 1:
            mid = len(arr) // 2
            L = arr[:mid]
            R = arr[mid:]

            _merge_sort(L)
            _merge_sort(R)

            i = j = k = 0

            while i < len(L) and j < len(R):
                if L[i] < R[j]:
                    arr[k] = L[i]
                    i += 1
                else:
                    arr[k] = R[j]
                    j += 1
                k += 1

            while i < len(L):
                arr[k] = L[i]
                i += 1
                k += 1

            while j < len(R):
                arr[k] = R[j]
                j += 1
                k += 1

    _merge_sort(arr)

def calc_area(length: int | float, width: int | float) -> int | float:
    if type(length) not in (int, float) or type(width) not in (int, float):
        raise TypeError("length and width must be int or float")
    if length <= 0 or width <= 0:
        raise ValueError("length and width must be positive")
    return length * width
